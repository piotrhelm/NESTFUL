from typing import Optional, Any



def check_availability(x: Optional[Any] = None) -> bool:

    """Checks if a given variable is not None and has a non-None value.



    Args:

        x: The variable to check.



    Returns:

        True if the variable is not None and has a non-None value, False otherwise.

    """

    if x is not None and x is not None:

        return True

    else:

        return False

