def ternary_like_condition(n: int) -> str:

    """Returns a string with a ternary-like condition: 'odd' if n is odd else 'even'.



    Args:

        n: The integer to check.



    Returns:

        A string: 'odd' if n is odd, 'even' if n is even.

    """

    return 'odd' if n % 2 == 1 else 'even'

