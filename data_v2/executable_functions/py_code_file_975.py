from typing import Union



def compute_a_plus_2b(a: Union[int, float], b: Union[int, float]) -> Union[int, float]:

    """Computes a + 2b using only the four arithmetic operations +, -, *, /.



    Args:

        a: The first integer.

        b: The second integer.



    Returns:

        The result of a + 2b.

    """

    return a + b + b

